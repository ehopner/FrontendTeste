public class Matematica {

    public double soma(final double parametroA, final double parametroB) {
        return parametroA + parametroB;
    }

    public double subtracao(double parametroA, double parametroB) {
        return parametroA - parametroB;
    }

    public double divisao(double parametroA, double parametroB) {
        return parametroA / parametroB;
    }

    public double multiplicacao(double parametroA, double parametroB) {
        return parametroA * parametroB;
    }

    public int restoDivisao(int parametroA, int parametroB) {
        return parametroA % parametroB;

    }

}